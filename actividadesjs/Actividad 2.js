function suma(num1, num2){
    var n;
    n = num1 + num2;
    alert("El resultado al sumarlo es: " + n);
    console.log("Hola mundo")
    return n;
}
suma(5,2);

function resta(num3, num4){
    var n2;
    n2 = num3 - num4;
    alert("El resultado al restarlo es: " + n2);
    return n2;
}
resta(5,2);